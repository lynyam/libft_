#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tail;

	tail = ft_lstlast(*lst);
	if (tail == NULL)
	{
		*lst = new;
		return ;
	}
	tail->next = new;
}
